package javaMethods;

public class MethodPractice1 {

	public static void main(String[] args) {
		int a = 10, b = 15;
		
		int addition = a + b;
		System.out.println("Addition is : " + addition);
		
		a = 55;
		b = 75;
		
		addition = a + b;
		System.out.println("Addition is : " + addition);
		
		a = 25;
		b = 35;
		
		addition = a + b;
		System.out.println("Addition is : " + addition);
		
	}

}
