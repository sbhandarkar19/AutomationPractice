package javaConditionStatement;

public class JavaIfStatement1 {
//	1) If Statement
//	Solve the problem of (If Statement) as per Requirement.

	public static void main(String[] args) {
		int marks = 25;
		
		if (marks >= 35) {                               // if [marks >=] 35 this is (if condition)
			System.out.println("You are passed");
		}
		
		if (marks < 35) {
			System.out.println("You are failed");
		}
	}

}
