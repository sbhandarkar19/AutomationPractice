package javaConditionStatement;

public class ConditionAllTheory {
	
//	1] ==   ---> To compare two values are equal or not and return true and false.
//	2] >    ---> Greater than.
//	3] <    ---> Less than.
//	4] >=   ---> Greater than and Equal to.
//	5] <=   ---> Less than and Equal to.
//	6] !=   ---> Not Equal to.
//	7] &&   ---> Logical AND.
//	8] ||   ---> Logical OR.
	
//	**********************************
	
//	7] && [logical AND] ---> if you want both the condition then use { AND Operator }.
	
//	true  && true   ==> true
//  false && true  ==> false
//	true  && false  ==> false
//	false && false ==> false
	
	
//	8] || [logical OR]  ---> if you want one of the two condition then use { OR Operator}.
	       
//	    true  || true   ==> true
//	    false || true  ==> true
//		true  || false  ==> true	
//      false || false ==> false
}
