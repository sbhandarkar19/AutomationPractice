package javaVariables;

public class VariablesPractice3 {

	public static void main(String[] args) {
//		how to declare and initialize the variable
//	    syntax declare + initialize--->dataType variableName = value;
		
		int i = 60;
		System.out.println(i);
		
		char ch = 'A';
		System.out.println(ch);
		
		char ch1 = 'B';
		char ch2 = 'C';
		char ch3 = 'D';
		
//		if we need to declare and/or initialize multiple variables of same dataType
		char c1 = 'a', c2 = 'b', c3, c4 = 'd', c5;
//		in above statement variables c3 & c5 are only declared and variables c1, c2 & c4 are declared and initialized
		
		boolean b1, b2, b3, b4, b5, b6;
		
		double d1 = 1.1, d2 = 1.2, d3 = 1.3, d4 = 1.4;
	}

}
