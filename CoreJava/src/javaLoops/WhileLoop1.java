package javaLoops;

public class WhileLoop1 {
//	WhileLoop / Entry Control Loop
//	Requirement ==> Print the table of 5 till 100

	public static void main(String[] args) {
		
		int i = 5;                      // Variable declare & initialize 5.
		
		while (i <= 100) {              // Condition 
			System.out.println(i);
			i = i + 5;                  // Operation
		}
     
	}

}
