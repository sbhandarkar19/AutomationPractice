package javaString;

public class StringPract1 {

	public static void main(String[] args) {
//		there are two of creating object of String class in java
//		1. by using String literal
//		2. by using new keyword
		
//		creating string object using [String literal]
		String str1 = "Hello World";
		
//		creating string object using [new keyword]
		String str2 = new String("Java Selenium");
		
		System.out.println(str1);
		System.out.println(str2);
	}
}
